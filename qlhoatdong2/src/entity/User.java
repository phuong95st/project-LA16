package entity;

/**
 * 
 * @author nguyenhuuphuong
 * 
 */
public class User {
	private int userId;
	private String email;
	private String phone;
	private String pass;
	private String fax;
	private String listSubject;
	private String releasedBook;
	private String other;
	private String releasedEngine;
	private String office;
	private String research;

	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}

	/**
	 * @param userId
	 *            the userId to set
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email
	 *            the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}

	/**
	 * @param phone
	 *            the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}

	/**
	 * @return the pass
	 */
	public String getPass() {
		return pass;
	}

	/**
	 * @param pass
	 *            the pass to set
	 */
	public void setPass(String pass) {
		this.pass = pass;
	}

	/**
	 * @return the fax
	 */
	public String getFax() {
		return fax;
	}

	/**
	 * @param fax
	 *            the fax to set
	 */
	public void setFax(String fax) {
		this.fax = fax;
	}

	/**
	 * @return the listSubject
	 */
	public String getListSubject() {
		return listSubject;
	}

	/**
	 * @param listSubject
	 *            the listSubject to set
	 */
	public void setListSubject(String listSubject) {
		this.listSubject = listSubject;
	}

	/**
	 * @return the releasedBook
	 */
	public String getReleasedBook() {
		return releasedBook;
	}

	/**
	 * @param releasedBook
	 *            the releasedBook to set
	 */
	public void setReleasedBook(String releasedBook) {
		this.releasedBook = releasedBook;
	}

	/**
	 * @return the other
	 */
	public String getOther() {
		return other;
	}

	/**
	 * @param other
	 *            the other to set
	 */
	public void setOther(String other) {
		this.other = other;
	}

	/**
	 * @return the releasedEngine
	 */
	public String getReleasedEngine() {
		return releasedEngine;
	}

	/**
	 * @param releasedEngine
	 *            the releasedEngine to set
	 */
	public void setReleasedEngine(String releasedEngine) {
		this.releasedEngine = releasedEngine;
	}

	/**
	 * @return the office
	 */
	public String getOffice() {
		return office;
	}

	/**
	 * @param office
	 *            the office to set
	 */
	public void setOffice(String office) {
		this.office = office;
	}

	/**
	 * @return the research
	 */
	public String getResearch() {
		return research;
	}

	/**
	 * @param research
	 *            the research to set
	 */
	public void setResearch(String research) {
		this.research = research;
	}

}
